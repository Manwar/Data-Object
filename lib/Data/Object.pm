# ABSTRACT: Data Type Objects for Perl 5
package Data::Object;

use 5.10.0;

use strict;
use warnings;

# VERSION

1;

=encoding utf8

=head1 SYNOPSIS

    use Data::Object;

=head1 DESCRIPTION

Data::Object provides functions for promoting to Perl 5 native data types to
objects which provide common methods for operating on the data.

